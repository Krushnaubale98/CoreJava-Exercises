package T6Operator;

// wap use prefix and postfix operators
public class excercise1 {

	public static void main(String[] args) {
		int var1 = 5;

		// use prefix using increment operator
		System.out.println("var1 posfix value is =" + var1++);
		System.out.println("var1 prefix value is=" + ++var1);
		System.out.println("var1 prefix value is=" + ++var1);
		System.out.println("var1 posfix value is =" + var1++);
	}

}
