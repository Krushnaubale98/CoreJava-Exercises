package T8NumberClass;

public class xxxValue {
// numbers supports the following datatype
	/* byte byteValue()
	 * short shortValue()
	 * int IntValue()
	 * long longValue()
	 * float floatValue()
	 * double doubleValue()
	 */
	public static void main(String[] args) {
		Integer x=5;
		
		// returns byte primitive data type
		System.out.println(x.byteValue());
		
		//return short primitive data types
		System.out.println(x.shortValue());
		
		//return int primitive dataType
		System.out.println(x.intValue());
		
		// return long primitive datatype
		System.out.println(x.longValue());
		
		//return float primitive datatype
		System.out.println(x.floatValue());
		
		//return double primitive dataype
		System.out.println(x.doubleValue());}

}
